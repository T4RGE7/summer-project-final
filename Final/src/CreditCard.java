
public class CreditCard {

	private String firstName, lastName, pan, expM, expY, security;

	public CreditCard(String firstName, String lastName, String pan, String expM, String expY, String security) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.pan = pan;
		this.expM = expM;
		this.expY = expY;
		this.security = security;
	}
	
	
	
}
